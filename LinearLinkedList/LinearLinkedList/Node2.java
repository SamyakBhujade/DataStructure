package LinearLinkedList;

public class Node2 {
	private int data;
	private Node next;
	
	public Node2(int data) {
		super();
		this.data = data;
		this.next = null;
	}
	
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	public Node getNext() {
		return next;
	}
	public void setNext(Node next) {
		this.next = next;
	}
	

}
